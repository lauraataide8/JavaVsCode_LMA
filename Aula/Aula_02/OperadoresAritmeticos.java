public class OperadoresAritmeticos {
    
    public static void main(String[] args) {
        double numero_01=3;
        double numero_02=3;
        double total;

        total= numero_01%numero_02;
        System.out.println("Total :"+total);
        
    }
}

/*
### Operadores Aritméticos
São usados para realizar operações matemáticas básicas. ###
+ : Adição (soma)

- : Subtração (diminui)

* : Multiplicação (multiplica)

/ : Divisão (divide)

% : Módulo (retorna o resto de uma divisão)

++ : Incremento (adiciona 1 ao valor)

-- : Decremento (subtrai 1 do valor)
*/
