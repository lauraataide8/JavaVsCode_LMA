
public class OperadoresComparativos {

    public static void main(String[] args) {
        double numero_01 = 9.0000000000000001;
        double numero_02 = 9;
        boolean resultado;

        resultado = numero_01 != numero_02;
        System.out.println("resultado :" + resultado);

    }
}
/*
### Operadores de Comparação (Relacionais)
São usados para comparar dois valores. O resultado é sempre um valor booleano (true ou false).

== : Igual a

!= : Diferente de

> : Maior que

< : Menor que

>= : Maior ou igual a

<= : Menor ou igual a
*/
